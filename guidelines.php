            <h1 style="font-size :300%"> What is My Health Record?</h1>
            <p style="font-size:150%;text-align:left"> My Health Record is an online summary of your health information. You control what goes into your 
                record, and who is allowed to access it. Share your health information with doctors, hospitals and
                 other healthcare providers from anywhere, any time.It keeps details like allergies, medical conditions
                  and treatments, medicine details, and test or scan results in one place.Health information stored in 
                  your record can include information from you, your healthcare providers, and Medicare.
                  You, or someone authorised to represent you, can add personal health summaries to your record to 
                  share additional information with your healthcare providers.  </p>
